export class CanvasState {
    src:any;
    width:number;
    height:number;
    shiftTop:number;
    shiftLeft:number;

    constructor(src:any, width:number, height:number, shiftTop:number, shiftLeft:number) {
        this.src = src;
        this.width = width;
        this.height = height;
        this.shiftTop = shiftTop;
        this.shiftLeft = shiftLeft;
    }
}



















